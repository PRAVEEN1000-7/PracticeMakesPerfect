import math

'''
1. Math Constants
math.pi → π (3.14159…)
math.e → Euler’s number (2.718…)
'''
print(math.pi)  # 3.141592653589793
print(math.e)   # 2.718281828459045

'''
2. Basic Functions
math.sqrt(x) → square root
math.pow(x, y) → x to the power y
math.factorial(n) → factorial (n!)
'''
print(math.sqrt(16))      # 4.0
print(math.pow(2, 3))     # 8.0
print(math.factorial(5))  # 120

'''
3. Rounding & Truncating
math.ceil(x) → round UP
math.floor(x) → round DOWN
math.trunc(x) → remove decimal part
'''
print(math.ceil(4.2))   # 5
print(math.floor(4.9))  # 4
print(math.trunc(4.9))  # 4

'''
4. Logarithms
math.log(x) → natural log (base e)
math.log10(x) → log base 10
math.log2(x) → log base 2
'''
print(math.log(10))    # 2.302...
print(math.log10(100)) # 2.0
print(math.log2(8))    # 3.0
